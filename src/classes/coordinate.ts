/**
 * The Coordinate
 */
export class Coordinate {
    /** True if the arrow is bent, false otherwise **/
    bend: boolean;

    /** The x-coordinate of the starting point of the arrow **/
    x1: number;

    /** The y-coordinate of the starting point of the arrow **/
    y1: number;

    /** The x-coordinate of the ending point of the arrow **/
    x2: number;

    /** The y-coordinate of the ending point of the arrow **/
    y2: number;
}
